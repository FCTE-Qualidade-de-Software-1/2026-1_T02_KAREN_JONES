import os
import subprocess
import sys
import time


def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    monitor_script = os.path.join(script_dir, "monitoria.py")
    ttft_script = os.path.join(script_dir, "finalTTFT.py")

    if not os.path.exists(monitor_script):
        raise FileNotFoundError(f"Arquivo não encontrado: {monitor_script}")
    if not os.path.exists(ttft_script):
        raise FileNotFoundError(f"Arquivo não encontrado: {ttft_script}")

    print("Iniciando monitoria de recursos...")
    monitor_proc = subprocess.Popen(
        [sys.executable, monitor_script],
        cwd=script_dir,
        stdout=sys.stdout,
        stderr=sys.stderr,
    )

    try:
        # Dá tempo para o monitor iniciar antes do teste TTFT
        time.sleep(3)

        print("Iniciando execução do TTFT...")
        ttft_result = subprocess.run(
            [sys.executable, ttft_script],
            cwd=script_dir,
            check=False,
        )

        if ttft_result.returncode != 0:
            print(f"O script finalTTFT.py terminou com código {ttft_result.returncode}")
        else:
            print("Execução de TTFT concluída com sucesso.")

    except KeyboardInterrupt:
        print("Interrupção recebida. Encerrando processos...")
    finally:
        if monitor_proc.poll() is None:
            print("Finalizando monitoria...")
            monitor_proc.terminate()
            try:
                monitor_proc.wait(timeout=5)
            except subprocess.TimeoutExpired:
                monitor_proc.kill()
        print("Processos encerrados.")


if __name__ == "__main__":
    main()























































































































































































































































































































































































































































































